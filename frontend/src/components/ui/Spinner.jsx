export default function Spinner({ size = 'md' }) {
  const sizes = {
    sm: 'h-4 w-4 border-2',
    md: 'h-8 w-8 border-4',
    lg: 'h-12 w-12 border-4',
  };
  const className = sizes[size] || sizes.md;
  return <span className={`inline-flex ${className} animate-spin rounded-full border-white/80 border-r-transparent`} />;
}
